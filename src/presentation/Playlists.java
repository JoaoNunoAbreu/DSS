    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        if(tabela.getSelectedRow()-1 >= 0){
            int row = tabela.convertRowIndexToModel(tabela.getSelectedRow()-1);
            String path = (String) model.getValueAt(row,4);
            tabela.setRowSelectionInterval(tabela.convertRowIndexToModel(row),tabela.convertRowIndexToModel(row));
            try {
                Runtime.getRuntime().exec("open -a /Applications/VLC.app " + path);
            } catch (IOException ex) {
                Logger.getLogger(Playlists.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jLabel6MouseClicked

